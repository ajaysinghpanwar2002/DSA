// 1D Dynamic Programming

// 1D Dynamic Programming involves optimizing solutions by breaking down problems into subproblems, which can be solved independently. The key distinction lies in the dimensionality of the table used to store the solutions. Unlike 2D DP that uses a matrix, 1D DP uses an array to store solutions to subproblems. This approach is particularly useful when the subproblems can be represented along a single dimension.

